// CriarPublicacao adiciona uma publicacao no banco de dados
func CriarPublicacao(w http.ResponseWriter, r *http.Request) {
	usuarioID, erro := autenticacao.ExtrairUsuarioID(r)
	if erro != nil {
		respostas.Erro(w, http.StatusUnauthorized, erro)
		return
	}

	// Lê o corpo da requisição para obter os dados da publicação
	corpoRequest, erro := ioutil.ReadAll(r.Body)
	if erro != nil {
		respostas.Erro(w, http.StatusBadRequest, erro)
		return
	}

	var publicacao models.Publicacao
	// Converte os dados JSON do corpo da requisição para a estrutura Publicacao
	if erro = json.Unmarshal(corpoRequest, &publicacao); erro != nil {
		respostas.Erro(w, http.StatusBadRequest, erro)
		return
	}

	// Valida a publicação
	if erro := publicacao.Validar(); erro != nil {
		respostas.Erro(w, http.StatusBadRequest, erro)
		return
	}

	// Associa a publicação ao usuário autenticado
	publicacao.AutorID = usuarioID

	db, erro := banco.ConectarDB()
	if erro != nil {
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}
	defer db.Close()

	repositorio := repositorios.NovaPublicacaoRepositorio(db)
	// Cria a publicação no banco de dados e obtém o ID da nova publicação
	publicacao.ID, erro = repositorio.Criar(publicacao)
	if erro != nil {
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}

	// Responde com sucesso, retornando a publicação criada
	respostas.JSON(w, http.StatusCreated, publicacao)

}

